<p>A laptop, laptop computer, or notebook computer is a small, portable personal computer (PC) with a screen and alphanumeric keyboard. These typically have a "clamshell" form factor, typically having the screen mounted on the inside of the upper lid of the clamshell and the keyboard on the inside of the lower lid, although 2-in-1 PCs with a detachable keyboard are often marketed as laptops or as having a "laptop mode." Laptops are folded shut for transportation, and thus are suitable for mobile use.[1] Its name comes from lap, as it was deemed practical to be placed on a person's lap when being used. Today, laptops are the used in a variety of settings, such as at work, in education, for playing games, web browsing, for personal multimedia, and general home computer use.</p>
